  const STORAGE_KEY = "solforge-language";
  const ATTRIBUTES = ["aria-label", "title", "alt", "placeholder", "content"];
  const SKIP_SELECTOR = "script, style, code, pre, textarea, [data-i18n-ignore]";
  const textMemory = new WeakMap();
  let currentLanguage = readStorage(STORAGE_KEY) === "en" ? "en" : "ko";
  let applying = false;

  function init() {
    createLanguageToggle();
    setLanguage(currentLanguage, { save: false });
    const observer = new MutationObserver((mutations) => {
      if (applying || currentLanguage !== "en") return;
      mutations.forEach((mutation) => {
        if (mutation.type === "childList") mutation.addedNodes.forEach((node) => applyNode(node));
        else if (mutation.type === "characterData") translateTextNode(mutation.target);
        else if (mutation.type === "attributes") translateAttributes(mutation.target);
      });
    });
    observer.observe(document.documentElement, { childList: true, subtree: true, characterData: true, attributes: true, attributeFilter: ATTRIBUTES });
  }

  function createLanguageToggle() {
    if (document.getElementById("languageToggle")) return;
    const themeToggle = document.getElementById("themeToggle");
    if (!themeToggle || !themeToggle.parentNode) return;
    const button = document.createElement("button");
    button.className = "language-toggle";
    button.type = "button";
    button.id = "languageToggle";
    button.dataset.i18nIgnore = "true";
    button.addEventListener("click", () => setLanguage(currentLanguage === "en" ? "ko" : "en", { save: true }));
    themeToggle.parentNode.insertBefore(button, themeToggle);
    updateToggle(button);
  }

  function setLanguage(language, options = {}) {
    const previousLanguage = document.documentElement.dataset.lang || "ko";
    currentLanguage = language === "en" ? "en" : "ko";
    if (options.save) writeStorage(STORAGE_KEY, currentLanguage);
    document.documentElement.lang = currentLanguage;
    document.documentElement.dataset.lang = currentLanguage;
    applying = true;
    if (currentLanguage === "en") applyNode(document.documentElement);
    else if (previousLanguage === "en") restoreNode(document.documentElement);
    applying = false;
    updateToggle(document.getElementById("languageToggle"));
  }

  function updateToggle(button) {
    if (!button) return;
    button.textContent = currentLanguage === "en" ? "KO" : "EN";
    button.setAttribute("aria-label", currentLanguage === "en" ? "Switch to Korean" : "Switch to English");
    button.setAttribute("aria-pressed", String(currentLanguage === "en"));
  }

  function applyNode(node) {
    if (!node) return;
    if (node.nodeType === Node.TEXT_NODE) { translateTextNode(node); return; }
    if (node.nodeType !== Node.ELEMENT_NODE || shouldSkip(node)) return;
    translateAttributes(node);
    const walker = document.createTreeWalker(node, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT, {
      acceptNode(target) {
        if (target.nodeType === Node.ELEMENT_NODE && shouldSkip(target)) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    let target = walker.currentNode;
    while (target) {
      if (target.nodeType === Node.TEXT_NODE) translateTextNode(target);
      else translateAttributes(target);
      target = walker.nextNode();
    }
  }

  function restoreNode(node) {
    if (!node) return;
    if (node.nodeType === Node.TEXT_NODE) { if (textMemory.has(node)) node.nodeValue = textMemory.get(node); return; }
    if (node.nodeType !== Node.ELEMENT_NODE) return;
    restoreAttributes(node);
    const walker = document.createTreeWalker(node, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT, {
      acceptNode(target) {
        if (target.nodeType === Node.ELEMENT_NODE && shouldSkip(target)) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    let target = walker.currentNode;
    while (target) {
      if (target.nodeType === Node.TEXT_NODE) { if (textMemory.has(target)) target.nodeValue = textMemory.get(target); }
      else restoreAttributes(target);
      target = walker.nextNode();
    }
  }

  function translateTextNode(node) {
    if (!node.parentElement || shouldSkip(node.parentElement)) return;
    const original = textMemory.get(node) || node.nodeValue;
    const translated = translateValue(original);
    if (translated !== original) {
      if (!textMemory.has(node)) textMemory.set(node, original);
      node.nodeValue = translated;
    }
  }

  function translateAttributes(element) {
    if (!element || shouldSkip(element)) return;
    ATTRIBUTES.forEach((attribute) => {
      if (!element.hasAttribute(attribute)) return;
      const storageName = "data-i18n-" + attribute + "-ko";
      const original = element.getAttribute(storageName) || element.getAttribute(attribute);
      const translated = translateValue(original);
      if (translated !== original) {
        if (!element.hasAttribute(storageName)) element.setAttribute(storageName, original);
        element.setAttribute(attribute, translated);
      }
    });
  }

  function restoreAttributes(element) {
    ATTRIBUTES.forEach((attribute) => {
      const storageName = "data-i18n-" + attribute + "-ko";
      if (element.hasAttribute(storageName)) element.setAttribute(attribute, element.getAttribute(storageName));
    });
  }

  function translateValue(value) {
    const text = String(value || "");
    const trimmed = text.trim();
    if (!/[\uAC00-\uD7A3]/.test(trimmed)) return text;
    const leading = text.match(/^\s*/)[0];
    const trailing = text.match(/\s*$/)[0];
    const translated = TRANSLATIONS[trimmed] || translatePattern(trimmed);
    return translated === trimmed ? text : leading + translated + trailing;
  }

  function translatePattern(text) {
    let output = text
      .replace(/(\d{4})\uB144\s*(\d{1,2})\uC6D4\s*(\d{1,2})\uC77C/g, (_, y, m, d) => y + "-" + String(m).padStart(2, "0") + "-" + String(d).padStart(2, "0"))
      .replace(/(\d{1,2})\uC6D4/g, "$1 month")
      .replace(/(\d+)\uB144/g, "$1 years")
      .replace(/(\d+)\uAC1C\uC6D4/g, "$1 months")
      .replace(/(\d+)\uC77C/g, "$1 days")
      .replace(/(\d+)\uAC1C/g, "$1 items")
      .replace(/(\d+)\uC138/g, "$1 years old")
      .replace(/(\d+)\uC810/g, "$1 points");
    return output;
  }

  function shouldSkip(node) {
    return node.matches?.(SKIP_SELECTOR) || Boolean(node.closest?.(SKIP_SELECTOR));
  }

  function readStorage(key) {
    try { return window.localStorage.getItem(key); } catch (_error) { return null; }
  }

  function writeStorage(key, value) {
    try { window.localStorage.setItem(key, value); } catch (_error) {}
  }

