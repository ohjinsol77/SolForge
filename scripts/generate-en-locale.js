function stripKoreanParticles(value) {
  return value
    .replace(/[은는이가을를와과의에에서으로로도만까지부터처럼보다]+/g, " ")
    .replace(/[·]+/g, " / ")
    .replace(/\s+/g, " ")
    .trim();
}

function fallback(value) {
  if (!/[가-힣]/.test(value)) return value;
  if (value.includes(" - SolForge")) {
    const title = value.replace(" - SolForge", "");
    return `${translate(title)} - SolForge`;
  }
  if (value.endsWith("입니다.")) return "This is a SolForge browser tool.";
  if (value.endsWith("하세요.")) return "Use this SolForge tool in your browser.";
  if (value.endsWith("합니다.")) return "This tool runs directly in your browser.";
  const converted = stripKoreanParticles(value);
  return /[가-힣]/.test(converted) ? "SolForge browser tool" : converted;
}

function tidy(value) {
  return value
    .replace(/\s+([.,:;!?])/g, "$1")
    .replace(/\s{2,}/g, " ")
    .replace(/\btool tool\b/gi, "tool")
    .replace(/\bcalculator calculator\b/gi, "calculator")
    .trim();
}

function translate(value) {
  if (!value || !/[가-힣]/.test(value)) return value;
  if (exact[value]) return exact[value];
  let output = value;
  for (const [koText, enText] of replacements.sort((a, b) => b[0].length - a[0].length)) {
    output = output.split(koText).join(enText);
  }
  output = output
    .replace(/합니다\./g, ".")
    .replace(/하세요\./g, ".")
    .replace(/입니다\./g, ".")
    .replace(/됩니다\./g, ".")
    .replace(/습니다\./g, ".")
    .replace(/세요\./g, ".")
    .replace(/입니다/g, "")
    .replace(/합니다/g, "")
    .replace(/됩니다/g, "")
    .replace(/세요/g, "")
    .replace(/[은는이가을를와과의에에서으로로도만까지부터]+/g, " ");
  output = tidy(output);
  return /[가-힣]/.test(output) ? fallback(value) : output;
}

function generate() {
  const existingEn = readJson("src/locales/en.json", {});
  const en = {};
  for (const [key, value] of Object.entries(ko)) {
    const current = existingEn[key];
    en[key] = current && !/[가-힣]/.test(current) ? current : translate(value);
  }

  writeJson("src/locales/en.json", Object.fromEntries(Object.entries(en).sort(([a], [b]) => a.localeCompare(b))));
  const remaining = Object.values(en).filter((value) => /[가-힣]/.test(value)).length;
  console.log(`Wrote src/locales/en.json (${Object.keys(en).length} keys, ${remaining} values still contain Hangul)`);
}

